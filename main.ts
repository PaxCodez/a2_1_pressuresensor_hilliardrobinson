let Value = 0
basic.forever(function () {
    Value = pins.analogReadPin(AnalogReadWritePin.P1)
    if (Value > 500) {
        basic.showLeds(`
            . # . # .
            . # . # .
            . . . . .
            # . . . #
            . # # # .
            `)
    } else if (Value < 500) {
        basic.showLeds(`
            . . . . .
            . . . . .
            # # # # #
            . . . . .
            . . . . .
            `)
    }
})
